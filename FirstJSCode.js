/*Join method*/
/*eta mulot ekta arrya'r element gula ke join korayh*/
const friends = ['rahim', 'kahim', 'fahim', 'mahim', 'sahim'];
console.log(friends.join()); // output : rahim,kahim,fahim,mahim,sahim
console.log(friends.join('|')); // output : rahim|kahim|fahim|mahim|sahim
console.log(friends.join('-')); // output : rahim-kahim-fahim-mahim-sahim